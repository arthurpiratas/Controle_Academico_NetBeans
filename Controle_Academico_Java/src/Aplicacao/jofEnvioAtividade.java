    private void jbEnviaAtividadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEnviaAtividadeActionPerformed
        // TODO add your handling code here:
        if(jtTurma.getSelectedRow() != -1){
            Rendimento_Escolar rendEsco = fachada.buscaRendimentoEscolar(aluno.getId(), fachada.buscaTurma(jtTurma.getValueAt(jtTurma.getSelectedRow(), 0).toString()).getId());
            if(jrbEnviar1.isSelected()){
                fachada.insereAtividadeAluno(aluno.getId(), fachada.buscaTurma(jtTurma.getValueAt(jtTurma.getSelectedRow(), 0).toString()).getId(), 0, jtAtv1.getText());
                limpaCampos();
                limpaTabela();
            }else if(jrbEnviar2.isSelected()){
                fachada.insereAtividadeAluno(aluno.getId(), fachada.buscaTurma(jtTurma.getValueAt(jtTurma.getSelectedRow(), 0).toString()).getId(), 1, jtAtv2.getText());
                limpaCampos();
                limpaTabela();
            }else if(jrbEnviar3.isSelected()){
                fachada.insereAtividadeAluno(aluno.getId(), fachada.buscaTurma(jtTurma.getValueAt(jtTurma.getSelectedRow(), 0).toString()).getId(), 2, jtAtv3.getText());
                limpaCampos();
                limpaTabela();
            }else if(jrbEnviar4.isSelected()){
                fachada.insereAtividadeAluno(aluno.getId(), fachada.buscaTurma(jtTurma.getValueAt(jtTurma.getSelectedRow(), 0).toString()).getId(), 3, jtAtv4.getText());
                limpaCampos();
                limpaTabela();
            }else{
                JOptionPane.showMessageDialog(rootPane, "Selecione uma Opção");
            }
        }
    }//GEN-LAST:event_jbEnviaAtividadeActionPerformed

    private void jtTurmaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtTurmaKeyReleased
        // TODO add your handling code here:
        
        if(jtTurma.getSelectedRow() != -1){
            try{
                    limpaCampos();
                    ativaBotao();
                    ativaTextFild();
                    Rendimento_Escolar rendEsco = fachada.buscaRendimentoEscolar(aluno.getId(), fachada.buscaTurma(jtTurma.getValueAt(jtTurma.getSelectedRow(), 0).toString()).getId());
                    jtAtv1.setText(rendEsco.getTrabalhos()[0].equals("Não Entregue") ? "" : rendEsco.getTrabalhos()[0]);
                    jtAtv2.setText(rendEsco.getTrabalhos()[1].equals("Não Entregue") ? "" : rendEsco.getTrabalhos()[1]);
                    jtAtv3.setText(rendEsco.getTrabalhos()[2].equals("Não Entregue") ? "" : rendEsco.getTrabalhos()[2]);
                    jtAtv4.setText(rendEsco.getTrabalhos()[3].equals("Não Entregue") ? "" : rendEsco.getTrabalhos()[3]);
                    
                    if(!(rendEsco.getTrabalhos()[0].equals("Não Entregue"))){
                        jtAtv1.setEditable(false);
                        jrbEnviar1.setEnabled(false);
                    }
                    if(!(rendEsco.getTrabalhos()[1].equals("Não Entregue"))){
                        jtAtv2.setEditable(false);
                        jrbEnviar2.setEnabled(false);
                    }
                    
                    if(!(rendEsco.getTrabalhos()[2].equals("Não Entregue"))){
                        jtAtv3.setEditable(false);
                        jrbEnviar3.setEnabled(false);
                    }
                    
                    if(!(rendEsco.getTrabalhos()[3].equals("Não Entregue"))){
                        jtAtv4.setEditable(false);
                        jrbEnviar4.setEnabled(false);
                    }
                    
                    
            }catch(NullPointerException e){        
                
           }
        }
        
    }//GEN-LAST:event_jtTurmaKeyReleased

