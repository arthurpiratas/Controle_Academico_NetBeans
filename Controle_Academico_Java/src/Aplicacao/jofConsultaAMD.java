    private void limpaTabela(){
        
        if (dtmADM.getRowCount() >= 0){
            for (int i=dtmADM.getRowCount();i > 0;i--){
                dtmADM.removeRow(0);
            }            
        }
    }
    
    private void limpaCampos(){
        admAux = null; 
        jtData.setText("");
        jtID.setText("");
        jtNome.setText("");
        jtNomeUsuario.setText("");
        jtSenha.setText("");
    }

