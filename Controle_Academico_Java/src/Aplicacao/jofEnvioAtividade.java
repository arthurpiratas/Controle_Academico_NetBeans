    private void limpaTabela() {
        
        if (dtmTurmas.getRowCount() >= 0){
            for (int i=dtmTurmas.getRowCount();i > 0;i--){
                dtmTurmas.removeRow(0);
            }            
        }
    }
    
    private void limpaCampos(){
        
        jtAtv1.setText("");
        jtAtv2.setText("");
        jtAtv3.setText("");
        jtAtv4.setText("");
    }
    
    private void ativaBotao(){
        jrbEnviar1.setEnabled(true);
        jrbEnviar2.setEnabled(true);
        jrbEnviar3.setEnabled(true);
        jrbEnviar4.setEnabled(true);
    }
    
    private void ativaTextFild(){
        jtAtv1.setEnabled(true);
        jtAtv2.setEnabled(true);
        jtAtv3.setEnabled(true);
        jtAtv4.setEnabled(true);
        jtAtv1.setEditable(true);
        jtAtv2.setEditable(true);
        jtAtv3.setEditable(true);
        jtAtv4.setEditable(true);
    }

